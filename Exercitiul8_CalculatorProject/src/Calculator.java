public class Calculator {

    //acesta este un constructor default
    //il folosim ca sa instantiem obiecte

    public Calculator(){

    }

    public int Adunare(int a, int b){
        return a + b;

    }

    public int Scadere(int a, int b){
        return a - b;
    }

    public int Inmultirea(int a, int b){
        return a * b;
    }
    public int Impartirea(int a, int b){
        return a / b;
    }

}
